char	*ft_strncpy(char *st1, char *st2, unsigned int size)
{
	unsigned int	i;

	i = 0;
	while (st1[i] && st2[i] && i < size)
	{
		str1[i] = st2[i];
		i++;
	}
	return (st1);
}
