int	ft_atoi(char *str)
{
    int	rtr;
	int ngt;

	ngt = 1;
	while ((*str >= 7 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			ngt = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		rtr = rtr * 10 + *str - 48;
		str++;
	}
	return (rtr * ngt);
}