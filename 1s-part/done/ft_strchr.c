char	*ft_strchr(char *str, int c)
{
	int	i;
	char *st;

	i = 0;
	while (*str)
	{
		if (*str == c)
		{
			st = str;
			break ;
		}
		else
			st = 0;
		str++;
	}
	return (st);
}