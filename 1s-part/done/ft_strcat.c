char	*ft_strcat(char *to, char *from)
{
	unsigned int	i;
	
	i = 0;
	while (to[i])
		i++;
	while (*from)
	{
		to[i] = *from;
		from++;
		i++;
	}
	to[i] = '\0';
	return (to);
}